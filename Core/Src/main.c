void MainThreadHandler(ULONG thread_input)
{
	while (1)
	{
		/* block semaphore_1*/
		tx_semaphore_get(&semaphore_1, TX_WAIT_FOREVER);
		
		LED1_GPIO_Port->ODR ^= LED1_Pin;
	}
}
//==============================================================================
void Thread_1_Handler(ULONG thread_input)
{
	while (1)
	{
		/* free semaphore_1*/
		tx_semaphore_put(&semaphore_1);
		
		tx_thread_sleep(1000);
	}
}
